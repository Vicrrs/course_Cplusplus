/*
cout é pronunciado "ver-out". Usado para saída e usa o operador de inserção (<<) - Em que é)

cin é pronunciado "see-in". Usado para entrada e usa o operador de extração (>>) - Em que é)
*/

#include <iostream>
using namespace std;

int main() {
    int x;
    cout << "Type Number: ";
    cin >> x;
    cout << "Your number is: " << x;
}
